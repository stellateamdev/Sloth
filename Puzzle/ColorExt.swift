import Foundation
import UIKit

extension UIColor {
    class func backgrounds() -> UIColor {
        return UIColor(red: 171/255, green: 214/255, blue: 218/255, alpha: 1)//UIColor(red: 152/255, green: 206/255, blue: 230/255, alpha: 1)
    }
    class func played() -> UIColor {
         return UIColor(red: 98/255, green: 132/255, blue: 147/255, alpha: 1)
    }
    class func play() -> UIColor {
         return UIColor(red: 1, green: 77/255, blue: 109/255, alpha: 1)
    }
}
